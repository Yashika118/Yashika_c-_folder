// SET ARE OF TWO TYPES:
// 1 ORDERED  2 UNORDERED

// ORDERED SET:
// contains unique elements
// /implemented using balanced BST
// elements are in sorted order
// random access is not possible i.e you cannot access ith element
// present in #include<set> header file

// MULTI SET
// can contains duplicates
// implemented using balanced BST
// elements are in sorted order
// random access is not possible
// #include<set> header file

// TIME COMPLEXITY OF ORDERED AND MULTI SET
// insertion: O(Log N)
// deletion: O(Log N)
// lower/upper_bound: O(Log N)

// UNORDERED SET
// contains unique element
// implemented using hashing
// elements are not in sorted order
// random access is not possible
// present in #include<unordered_set> header file

// TIME COMPLEXITY OF UNORDERED SET
// insertion: O(1) in avg case, O(N) in worst case
// deletion: O(1) in avg case, O(N) in worst case
// LOWER/UPPER_BOUND: NA